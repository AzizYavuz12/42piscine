int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = 0;
	j = nb;
	while (j >= 1)
	{
		if (nb <= 0)
			return (0);
		if (nb == 1)
			return (1);
		else if (i * i == nb)
		{
			return (i);
		}
		j--;
		i++;
	}
	return (0);
}
